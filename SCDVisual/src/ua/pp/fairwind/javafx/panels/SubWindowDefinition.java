package ua.pp.fairwind.javafx.panels;

public class SubWindowDefinition {
	String keyString;
	String titleString;
	String hoverString;
	String urlForLoadScreen;

	public SubWindowDefinition() {
		super();
	}

	public String getKeyString() {
		return keyString;
	}

	public void setKeyString(String keyString) {
		this.keyString = keyString;
	}

	public String getTitleString() {
		return titleString;
	}

	public void setTitleString(String titleString) {
		this.titleString = titleString;
	}

	public String getHoverString() {
		return hoverString;
	}

	public void setHoverString(String hoverString) {
		this.hoverString = hoverString;
	}

	public String getUrlForLoadScreen() {
		return urlForLoadScreen;
	}

	public void setUrlForLoadScreen(String urlForLoadScreen) {
		this.urlForLoadScreen = urlForLoadScreen;
	}
	
	
}
